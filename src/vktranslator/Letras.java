package vktranslator;

/**
 *
 * @author Administrador
 */
public class Letras {
    public String encode;
    public String traduzido;

    /**
     * @return the encode
     */
    public String getEncode() {
        return encode;
    }

    /**
     * @param encode the encode to set
     */
    public void setEncode(String encode) {
        this.encode = encode;
    }

    /**
     * @return the traduzido
     */
    public String getTraduzido() {
        return traduzido;
    }

    /**
     * @param traduzido the traduzido to set
     */
    public void setTraduzido(String traduzido) {
        this.traduzido = traduzido;
    }
}
